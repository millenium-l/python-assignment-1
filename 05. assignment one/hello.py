# input from the user
num1 = int(input("Enter num 1: "))
num2 = int(input("Enter num 2: "))

# perform operations
addition = num1 + num2
subtraction = num1 - num2
multiplication =  num1 * num2
division = num1 / num2

# print the outcome 
print(f"Result of addition is: {addition}\n")
print(f"Result of subtraction is: {subtraction}\n")
print(f"Result of multiplication is: {multiplication}\n")
print(f"Result of division is: {division}\n")